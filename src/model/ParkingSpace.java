package model;

public class ParkingSpace {

	public int idParking_Space;
	public int floor;
	public int Parking_Slot;
	public String status;
	public int getIdParking_Space() {
		return idParking_Space;
	}
	public void setIdParking_Space(int idParking_Space) {
		this.idParking_Space = idParking_Space;
	}
	public int getFloor() {
		return floor;
	}
	public void setFloor(int floor) {
		this.floor = floor;
	}
	public int getParking_Slot() {
		return Parking_Slot;
	}
	public void setParking_Slot(int parking_Slot) {
		Parking_Slot = parking_Slot;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	
	
	
	
	
}
